<h2><p><font color="blue">

Cotton was mijn eerste vogel als huisdier. We kregen hem van mijn vader omdat mijn vaders vriend was gestorven.
Hij bleef bij ons een jaartje. We hadden maar een jaartje met hem omdat zijn bloedvaten dichtwaren. We hadden dit niet opgemerkt.
op een dag begon Cotton raar te schreeuwen. We gaven hem medicijn maar het lukte niet. Mijn broer bracht hem naar de vogel kliniek
