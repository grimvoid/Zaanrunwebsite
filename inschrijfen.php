<h1><p><font color="green">Nina </font></p></h1>

<h2><p><font color="green">

Nina was mijn tweede vogel. We hadden haar gekocht omdat De eerste vogel Cotton zich alleen voelde.
Ze was altijd lief tegen Cotton. Als hij jeuk had dat krabt ze hem achter zijn nek. Maar als het om eten gaat dan
bijt ze altijd deze vogel. Nina wilt vaak niet delen met andere vogels. Daarom heb ik twee voerbakken geplaats ver van elkaar.
Nina is niet kieskeurig over haar voedsel. Ze eet bijna van alles en nog wat. Toen ik 1 keer van school kwam zag ik haar aan de plant te kauwen.
Ook bijt ze vaak aan de kooi. Ze wilt sterk zijn dus ze moet alle ijzer binnen krijgen. Alles was goed totdat Cotton stierf.
Nina was erg verdrietig over dit. Dus kochten we voor haar een nieuwe mannelijke vogel Sneeuw. Alles was goed totdat deze vogel wegvloog.
Nina kon het niet meer aan dus vloog ze ook weg. Ik hoop echt dat alles goed is met haar.

</font></p></h2>

