<h1><p><font color="red">sneeuw </font></p></h1>

<h2><p><font color="red">

Sneeuw was onze tweede mannelijke vogel. Hij was 4 maanden oud toen we hem kochten uit de Pets Place XL in IJburg.
Hij was vol met kracht en passie. inplaats van vliegen rent deze vogel vaak buiten de kooi. Deze vogel was vaak niet voorzichtig.
Hij sprong altijd hoog van de kooi. 1 keer bloedde zijn snavel. Hij heet sneeuw omdat hij wit als sneeuw heet.
Hij was ook de pluisichte van alle vogels. Hij voelde net als een kussen als ik hem aanraakte. voordat ik naar indonesia ging
Deed mijn moeder de kleren buiten. Uit het niets kwam een reiger op onze balkon.Sneeuw schrok hier erg van en vloog weg.
we probeerden hem te vinden maar het lukte niet. We hoorden hem schreeuwen maar we zagen hem niet.
de andere vogel Nina was erg verdrietig hierover. Toen ik in indonesia was vloog ze weg van huis. Ik zag haar nooit meer.
We hadden toen de vogel Haru gekocht maar die was eenzaam 3 weken alleen bij een van mijn vaders vrienden.

</font></p></h2>

